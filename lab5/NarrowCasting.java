package Samplepackage;

public class NarrowCasting {
	    public static void main(String[] args) 
	    {
	        double Bhuvan = 143.143; 
	        int Dev = (int) Bhuvan;

	        System.out.println("Original double value: " + Bhuvan);
	        System.out.println("Narrowed int value: " + Dev);
	    }
}